package for_i;

import java.util.Scanner;

public class For_1 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int k = scanner.nextInt();
        int n = scanner.nextInt();
        for (int i = 0; i < n; i++) {
            System.out.println(k);
        }
    }
}
